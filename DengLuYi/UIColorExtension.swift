import UIKit

extension UIColor {
    class func dlyGreenColor() -> UIColor {
        return UIColor(red: 0.0, green: 186.0 / 255.0, blue: 138.0 / 255.0, alpha: 1.0)
    }
    
    class func dlyTitleFontColor() -> UIColor {
        return UIColor(red: 34.0 / 255.0, green: 34.0 / 255.0, blue: 34.0 / 255.0, alpha: 1.0)
    }
    
    class func dlyBodyFontColor() -> UIColor {
        return UIColor(red: 85.0 / 255.0, green: 85.0 / 255.0, blue: 85.0 / 255.0, alpha: 1.0)
    }
}
